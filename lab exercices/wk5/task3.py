# This program will calculate my minimum and best grades in my last math course.
math_grades = [93.31, 71.89, 85.05] # here are my grades
lower = min(math_grades) #Output my lower score in last semester
best = max(math_grades)  #Best score in last semester
print(f"My lower grade in Math was: {lower}")
print(f"My best grade in Math was: {best}")